let x = 5;
let y = 3;

// addition
console.log('x + y = ', x + y); 

// subtraction
console.log('x - y = ', x - y);  

// multiplication
console.log('x * y = ', x * y); 

// division
console.log('x / y = ', x / y); 

// remainder
console.log('x % y = ', x % y); 

// increment
console.log('++x = ', ++x); // x is now 6
console.log('x++ = ', x++); // prints 6 and then increased to 7
console.log('x = ', x);     // 7

// decrement
console.log('--x = ', --x); // x is now 6
console.log('x-- = ', x--); // prints 6 and then decreased to 5
console.log('x = ', x);     // 5

//exponentiation
console.log('x ** y =', x ** y);